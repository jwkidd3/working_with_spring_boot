package com.example.taskapi.entity;

/**
 * Enumeration representing the priority level of a task.
 */
public enum TaskPriority {
    /**
     * Low priority task
     */
    LOW,

    /**
     * Medium priority task
     */
    MEDIUM,

    /**
     * High priority task
     */
    HIGH,

    /**
     * Critical priority task requiring immediate attention
     */
    CRITICAL
}
